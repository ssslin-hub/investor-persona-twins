"""Build reports/eval_summary.md — one consolidated cross-version comparison.

Fair common config: K=10, judge = gpt-5, n=12 actuals (2026-Q1 holdout),
5 seeds per version. Pulls the per-run b2_summary.json (aggregate B2 0-4 +
binary match rate) and b2_per_actual.json (per-analyst best-match score) that
already exist under data_auto/, and joins actual question text from
data/analysts_test.json.

v7 is intentionally excluded: its generations are DRY_RUN stubs (no live model
run / scoring yet), so a v7 accuracy number would be meaningless.

Usage:
  python3 src/build_eval_summary.py            # writes reports/eval_summary.md
  python3 src/build_eval_summary.py --print     # also print to stdout
"""

from __future__ import annotations

import argparse
import json
import os
import statistics as st

HERE = os.path.dirname(os.path.abspath(__file__))
ROOT = os.path.dirname(HERE)
DATA = os.path.join(ROOT, "data")
DA = os.path.join(ROOT, "data_auto")
OUT = os.path.join(ROOT, "reports", "eval_summary.md")

# version label -> (description, list of 5 seed-run dirs scored by gpt-5 @ K=10)
VERSIONS = {
    "v1": ("baseline persona (recurring_concerns only)",
           [f"final_eval_10q_v1/v5_compare/gpt-5/run_{i}" for i in range(1, 6)]),
    "v5": ("structured persona (queue/reactivity)",
           [f"final_eval_10q_v5/v5_compare/gpt-5/run_{i}" for i in range(1, 6)]),
    "v6": ("v5 + asset/theme cross-references",
           [f"final_eval_10q_v6/variance/run_{i}" for i in range(1, 6)]),
    "auto": ("auto-discovered persona",
             [f"final_eval_10q_auto/v5_compare/gpt-5/run_{i}" for i in range(1, 6)]),
}

K = 10
JUDGE = "gpt-5"


def _summary(dirpath: str) -> dict | None:
    p = os.path.join(DA, dirpath, "b2_summary.json")
    if not os.path.exists(p):
        return None
    return json.load(open(p))


def _cells(dirpath: str) -> list[dict]:
    p = os.path.join(DA, dirpath, "b2_per_actual.json")
    if not os.path.exists(p):
        return []
    d = json.load(open(p))
    return d["cells"] if isinstance(d, dict) and "cells" in d else (d if isinstance(d, list) else [])


def actual_questions() -> dict[str, str]:
    """analyst_name -> first actual question text (2026-Q1 holdout)."""
    t = json.load(open(os.path.join(DATA, "analysts_test.json")))
    out = {}
    for name, qs in t["per_analyst_actual_questions"].items():
        if qs:
            out[name] = qs[0]["question"]
    return out


def _snip(s: str, n: int = 90) -> str:
    s = " ".join((s or "").split())
    return s if len(s) <= n else s[: n - 1] + "…"


def build() -> str:
    lines: list[str] = []
    lines.append("# Evaluation summary — actual vs best-predicted (cross-version)")
    lines.append("")
    lines.append(f"Common config: **K={K}**, judge **{JUDGE}**, **n=12** actual questions "
                 f"(2026-Q1 holdout), **5 seeds** per version. Score = B2 match "
                 f"`match_score_0_to_4` (4 = strong match). Generated by "
                 f"`src/build_eval_summary.py`.")
    lines.append("")
    lines.append("> v7 is omitted: its generations are DRY_RUN stubs (no live model run / "
                 "scoring yet). Run live generation + the judge before adding a v7 row.")
    lines.append("")

    # ---- aggregate table ----
    lines.append("## Aggregate (mean over 5 seeds)")
    lines.append("")
    lines.append("| Version | Description | Mean B2 (0–4) | Range | Binary match rate | Range |")
    lines.append("|---|---|---|---|---|---|")
    agg = {}
    for v, (desc, dirs) in VERSIONS.items():
        avgs, bins = [], []
        for d in dirs:
            s = _summary(d)
            if s and s.get("average_match_score_0_to_4") is not None and s.get("evaluated_count") == 12:
                avgs.append(s["average_match_score_0_to_4"])
                bins.append(s.get("binary_match_rate", 0))
        if not avgs:
            lines.append(f"| {v} | {desc} | — | — | — | — |")
            continue
        agg[v] = (st.mean(avgs), st.mean(bins))
        lines.append(f"| **{v}** | {desc} | **{st.mean(avgs):.2f}** | "
                     f"{min(avgs):.2f}–{max(avgs):.2f} | {st.mean(bins):.0%} | "
                     f"{min(bins):.0%}–{max(bins):.0%} |")
    lines.append("")

    # ---- per-analyst table (run_1 of each version) ----
    actuals = actual_questions()
    lines.append("## Per-analyst best-match score (seed run_1)")
    lines.append("")
    lines.append("Each cell = the best-matching predicted candidate's B2 score (0–4) for that "
                 "analyst's actual 2026-Q1 question.")
    lines.append("")
    header = "| Analyst | Actual question (2026-Q1) | " + " | ".join(VERSIONS) + " |"
    lines.append(header)
    lines.append("|" + "---|" * (2 + len(VERSIONS)))

    # collect per-analyst scores from run_1
    per_v_scores: dict[str, dict[str, int]] = {}
    analysts_seen: list[str] = []
    for v, (_, dirs) in VERSIONS.items():
        cells = _cells(dirs[0])
        m = {}
        for c in cells:
            name = c.get("analyst_name")
            m[name] = c.get("match_score_0_to_4")
            if name not in analysts_seen:
                analysts_seen.append(name)
        per_v_scores[v] = m

    for name in analysts_seen:
        actual = _snip(actuals.get(name, ""))
        row = [name, actual]
        for v in VERSIONS:
            sc = per_v_scores[v].get(name)
            row.append("—" if sc is None else str(sc))
        lines.append("| " + " | ".join(row) + " |")
    lines.append("")

    # ---- provenance ----
    lines.append("## Provenance (exact source files)")
    lines.append("")
    for v, (_, dirs) in VERSIONS.items():
        lines.append(f"- **{v}**: `data_auto/{dirs[0]}/` … `run_5/` "
                     f"(`b2_summary.json`, `b2_per_actual.json`, `raw_pool.json`)")
    lines.append("")
    lines.append("Per-run detail (full predicted pools + per-candidate reasoning) is in the "
                 "committed `raw_pool.json` / `b2_per_actual.json` for these runs, and in the "
                 "narrative reports `reports/v1_k10_actual_vs_predicted.md`, "
                 "`reports/auto_k10_actual_vs_predicted_gpt5.md`, "
                 "`reports/v5k20_actual_vs_gpt5pick.md`.")
    return "\n".join(lines) + "\n"


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--print", action="store_true", dest="do_print")
    args = ap.parse_args()
    md = build()
    with open(OUT, "w") as fp:
        fp.write(md)
    print(f"wrote {OUT}")
    if args.do_print:
        print("\n" + md)


if __name__ == "__main__":
    main()
